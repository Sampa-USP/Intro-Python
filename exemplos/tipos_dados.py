"""
Neste exemplo, o programa solicita ao usuário a entrada de 2 números inteiros usando a função input() e converte esses valores para o tipo int usando a função int(). Em seguida, o programa realiza as operações matemáticas de soma, subtração, multiplicação e divisão usando os operadores +, -, * e /. Por fim, os resultados são impressos na tela usando a função print().
"""



# Solicita a entrada do usuário para os números inteiros
numero1 = int(input("Digite o primeiro número inteiro: "))
numero2 = int(input("Digite o segundo número inteiro: "))

# Realiza as operações matemáticas
soma = numero1 + numero2
subtracao = numero1 - numero2
multiplicacao = numero1 * numero2
divisao = numero1 / numero2

# Imprime os resultados na tela
print("Soma:", soma)
print("Subtração:", subtracao)
print("Multiplicação:", multiplicacao)
print("Divisão:", divisao)
